package com.yongda.site.service.personal.facade.response;

public class PersonalTradeResponse {

	private String amount;

	private String fundout_order_no;
	
	private String type;
	
	
	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getFundout_order_no() {
		return fundout_order_no;
	}

	public void setFundout_order_no(String fundout_order_no) {
		this.fundout_order_no = fundout_order_no;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	
}
